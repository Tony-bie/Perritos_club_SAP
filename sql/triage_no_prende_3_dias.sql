-- 2) Ultimos 20 runs. Si aqui no hay nada reciente, el worker no esta corriendo
-- o no puede llegar a SAP SOC antes de guardar el run.
SELECT '02_ULTIMOS_RUNS' AS SECTION FROM DUMMY;

