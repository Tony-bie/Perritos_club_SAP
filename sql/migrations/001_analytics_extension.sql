CREATE OR REPLACE VIEW VW_LATEST_ALERTS AS
SELECT
    ALERT_ID,
    RUN_ID,
    DETECTED_AT_UTC,
    ALERT_TYPE,
    SEVERITY,
    PAYLOAD
FROM (
    SELECT
        A.ALERT_ID,
        A.RUN_ID,
        A.DETECTED_AT_UTC,
        A.ALERT_TYPE,
        A.SEVERITY,
        A.PAYLOAD,
        ROW_NUMBER() OVER (
            ORDER BY A.DETECTED_AT_UTC DESC, A.ALERT_ID DESC
        ) AS RN
    FROM ALERTS_EVENTS A
)
WHERE RN <= 100;

CREATE OR REPLACE VIEW VW_RISK_TIMELINE AS
SELECT
    WINDOW_KEY,
    WINDOW_START,
    WINDOW_END,
    TOTAL_RECORDS,
    THREAT_SCORE,
    DETECTION_COUNT,
    ATTACK_PREDICTED,
    MODEL_AVAILABLE,
    IS_ANOMALY,
    ANOMALY_SCORE,
    ANOMALY_PERCENTILE,
    SAVED_AT_UTC
FROM WINDOW_METRICS;

CREATE OR REPLACE VIEW VW_MODEL_PERFORMANCE AS
SELECT
    MR.RUN_ID,
    MR.ALGORITHM,
    MR.FEATURE_TABLE,
    MR.TRAINING_ROW_COUNT,
    MR.CONTAMINATION,
    MR.STATUS,
    COUNT(MP.PREDICTION_ID) AS TOTAL_PREDICTIONS,
    AVG(MP.SCORE) AS AVG_SCORE,
    AVG(MP.CONFIDENCE) AS AVG_CONFIDENCE,
    SUM(
        CASE
            WHEN UPPER(COALESCE(MP.PREDICTED_LABEL, '')) IN ('ANOMALY', 'TRUE', 'YES', '1')
            THEN 1 ELSE 0
        END
    ) AS ANOMALY_PREDICTIONS,
    MAX(MP.PREDICTED_AT_UTC) AS LAST_PREDICTION_AT_UTC
FROM MODEL_RUNS MR
LEFT JOIN MODEL_PREDICTIONS MP
    ON MR.RUN_ID = MP.RUN_ID
GROUP BY
    MR.RUN_ID,
    MR.ALGORITHM,
    MR.FEATURE_TABLE,
    MR.TRAINING_ROW_COUNT,
    MR.CONTAMINATION,
    MR.STATUS;

CREATE OR REPLACE VIEW VW_ALERT_SUMMARY AS
SELECT
    ALERT_TYPE,
    SEVERITY,
    COUNT(*) AS TOTAL_ALERTS,
    MAX(DETECTED_AT_UTC) AS LAST_DETECTED_AT_UTC
FROM ALERTS_EVENTS
GROUP BY ALERT_TYPE, SEVERITY;