BEGIN
    DECLARE v_count INTEGER;

    SELECT COUNT(*) INTO v_count
    FROM SYS.TABLES
    WHERE SCHEMA_NAME = CURRENT_SCHEMA
      AND TABLE_NAME = 'MODEL_RUNS';

    IF :v_count = 0 THEN
        EXECUTE IMMEDIATE '
            CREATE COLUMN TABLE MODEL_RUNS (
                RUN_ID NVARCHAR(64) PRIMARY KEY,
                ALGORITHM NVARCHAR(120) NOT NULL,
